#include "vect3d.h"

t_vect3d	vect3d_cross(t_vect3d v1, t_vect3d v2)
{
	return (vect3d(v1[Y] * v2[Z] - v1[Z] * v2[Y],
					v1[Z] * v2[X] - v1[X] * v2[Z],
					v1[X] * v2[Y] - v1[Y] * v2[X]));
}
