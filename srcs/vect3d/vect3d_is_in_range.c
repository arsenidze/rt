#include "vect3d.h"

short		vect3d_is_in_range(t_vect3d v, double a, double b)
{
	return ((v[X] >= a && v[X] <= b)
		&& (v[Y] >= a && v[Y] <= b)
		&& (v[Z] >= a && v[Z] <= b));
}
