#include "vect3d.h"

t_vect3d	vect3d_mult_on_matrix(t_vect3d v, t_vect3d m[3])
{
	t_vect3d	res;

	res[X] = m[0][0] * v[X] + m[0][1] * v[Y] + m[0][2] * v[Z];
	res[Y] = m[1][0] * v[X] + m[1][1] * v[Y] + m[1][2] * v[Z];
	res[Z] = m[2][0] * v[X] + m[2][1] * v[Y] + m[2][2] * v[Z];
	return (res);
}
